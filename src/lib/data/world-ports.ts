function uniqueSorted(names: string[]) {
  return [...new Set(names.map((n) => n.trim()).filter(Boolean))].sort((a, b) =>
    a.localeCompare(b, "ar")
  );
}

const allPorts = uniqueSorted([...PINNED_PORT_NAMES, ...WORLD_PORTS_RAW]);

const pinnedSet = new Set<string>(PINNED_PORT_NAMES);

export const PORT_OPTIONS = [
  ...PINNED_PORT_NAMES.filter((p) => allPorts.includes(p)),
  ...allPorts.filter((p) => !pinnedSet.has(p)),
];

export function sortPortsForPicker(ports: string[]) {
  const set = new Set(ports);
  const pinned = PINNED_PORT_NAMES.filter((p) => set.has(p));
  const rest = [...set].filter((p) => !pinnedSet.has(p)).sort((a, b) => a.localeCompare(b, "ar"));
  return [...pinned, ...rest];
}
